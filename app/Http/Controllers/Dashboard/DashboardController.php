            ->select('type_id', 'app_masters.name', DB::raw('COUNT(employee_contracts.id) as jumlah'))
            ->join('app_masters','employee_contracts.type_id', 'app_masters.id')
            ->groupBy('type_id')
            ->get();
        $data['empType'] = json_encode($empType);

        return view('dashboards.dashboards.index', $data);
